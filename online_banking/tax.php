        <p>If you are among those who rush to buy tax saving plans at the last-minute, you may inadvertently end up hurting your long-term financial growth. Buying tax saving plans for the sole purpose of reducing your tax burden, puts your financial health at stake. Therefore, it is essential to align your long-term financial goals with tax planning instruments. By doing so, you can maximize returns as well as save up on taxes. All of this cannot be done in a rush. The secret of effective tax planning is starting well ahead of time. To make sure your tax planning is aligned with your financial goals, we have listed down tax saving plans according to long-term financial goals.<br></p><br>

           <h4><b  style="color:red;">Getting Insured</b></h4>
           <p>Life is full of unexpected twists and turns. Although many factors are beyond your control, taking up insurance is the best to avoid financial contingencies. Both life insurance and health insurance plans offer amazing tax benefits</p>

           <ul class="list-points">
            <li><b>Life insurance</b> - Premium paid towards life insurance plans are eligible for tax deductions up to Rs 1.5 lakh under Section 80C of the Income Tax Act. If you purchased a life insurance policy after 1 April 2012, the premium should be less than 10 percent sum insured to claim a tax deduction. Find out more about <a href="/content/kotakcl/en/personal-banking/insurance/life-insurance.html" target="_self">Life Insurance</a> and get yourself insured</li>
            <br>
            <li><b>Health insurance</b> - Premium paid towards health insurance policies of self, spouse and dependent children are eligible for tax deductions up to Rs. 25,000 every financial year. In case the insured is a senior citizen the deduction is extended to Rs. 50,000.You can view the complete details of <a href="/content/kotakcl/en/personal-banking/insurance/health-insurance.html" target="_self">Health Insurance</a> to know more.</li>
            </ul>     
                   
            <p><b><br>Advantage of paying premium for long Term health insurance</b></p>
            <ul class="list-points">
                <li>In case a customer is paying health insurance premium for 2 year/ 3 year terms, he is not only eligible for long term discount (maximum up to 5%) also he can claim tax deduction for the premium paid in yearly instalments for next 3 years</li>
                <li>Illustrations -&nbsp;A customer who is aged 45 years buys health insurance for 3 year period &amp; he pays premium of Rs. 60,000 as a single premium. Every year he can claim Rs. 20,000/- deduction under section 80D for next 3 years. Before FY 18/19, in such instance, customer was allowed to take only once the benefit of Section 80D of amount Rs. 25,000 only &nbsp;&nbsp;</li>
                </ul><br>

                <h4><b  style="color:red;">Boost Wealth/Savings</b></h4>
                <p>Long term investments are a great way to enhance your wealth. Investing in these tax saving plans are a great way to boost your wealth as well as save up on taxes.</p>
                <ul class="list-points">
                <li><b>Tax Saving Fixed Deposits</b> - Fixed deposits are among the safest and rewarding long term investment plans. Earn attractive interest and save up on taxes simultaneously with fixed deposits. You can invest Rs. 1 lakh to Rs. 1.5 under Tax Saving Fixed Deposits with a lock-in period of 5 years. Find out more about the <a href="/content/kotakcl/en/personal-banking/deposits/tax-saving-fixed-deposit.html" target="_self">tax saving fixed deposit</a> associated with Kotak.</li>
                <br>
                <li><b>Rajiv Gandhi Equity Savings Scheme or RGESS</b> - The RGESS is an equity based investment plan that offers considerable tax benefits. If you are a first time investor, you can claim 50 percent of the investment as a deduction for shares purchased up to Rs. 50,000 under Section 80CCG of the IT Act</li>
                <br>
                <li><b>Equity Linked Savings Scheme or ELSS</b> - The ELSS is an equity linked saving scheme which offers tax benefits under the section 80C of the IT Act. However, there is a lock in of three years from the date of investment. The long term capital gains on such investment would be taxed at 10% plus applicable surcharge &amp; cess</li>
                <br>
                <li><b>Unit-linked insurance plans or ULIPS</b> - ULIPs are insurance cum investment plans which are eligible for tax benefits under the Section 80C of the IT Act. Additionally, the proceeds from this investment plan are exempt from tax if the insurance cover is 10 times or more of the highest annual premium paid in any policy year for policy taken after 1st April 2012.</li>
                </ul><br>

                <h4><b style="color:red;">Retirement Planning</b></h4>
                <p>Planning for the golden years of life is one of the most important financial goals for every individual. The earlier you start, the better it is. Thanks to certain saving plans, you can save for your retirement as well as save on taxes. We have listed down some of the best tax saving plans for retirement planning right here.</p>
                <ul class="list-points">
                <li><b>Employees' Provident Fund or EPF</b> - The EPF is only applicable for salaried individuals. If you are a salaried individual, you may already have an EPF account. The contribution made towards your EPF is eligible for tax deductions up to Rs. 1.5 lakh under the section 80C of the income tax act</li>
                <br>
                <li><b>National Savings Certificates or NSCs</b> - The NSC is a long-term savings scheme that offers attractive interest rates and tax benefits. They are available as 5-year and 10-year plans and offer 8.6 percent and 8.9 percent interest respectively. It is eligible for tax deductions under Section 80C of the IT Act</li>
                <br>
                <li><b>National Pension System or NPS</b> - &nbsp;NPS is one of the low cost Unit linked pension plan that offers tax benefit upto Rs. 1.5 lakh under section 80C and additional tax benefits up to Rs. 50,000 exclusively for NPS(Tier I account) &nbsp;under subsection 80CCD (1B). Check out the criteria to avail in the <a href="/content/kotakcl/en/personal-banking/investments/national-pension-system.html" target="_self">National Pension System or NPS</a>.</li>
                </ul><br>



                <h4><b style="color:red;">Indirect Savings</b></h4>
                <ul class="list-points">
                <li><b>Home Loans</b> -If you have taken up a home loan, you will be glad to know that it entails attractive tax benefits. You can enjoy tax benefits ranging from Rs. 1 lakh on the principal amount and Rs. 1.5 lakh on the interest amount under the section 80C and section 24 respectively. Furthermore, home loans taken-up for the purpose of renovation are also eligible for tax benefits. Find out more details about the <a href="/content/kotakcl/en/personal-banking/loans/home-loan.html" target="_self">Home Loan</a> associated with Kotak Bank.</li>
                </ul><br><br> 
